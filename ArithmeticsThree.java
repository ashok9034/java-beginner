package com.java.beginners;

public class ArithmeticsThree {
	
	public static void main(String[] args) {
		
		int a = 1;
		int b = 2; 
		int c = 3;
		
		System.out.println(a+b-c);
		System.out.println(a-b+c);
		System.out.println(a*b*c);
		System.out.println(c*b/a);
		System.out.println(c*b%a);
		
	}

}
